					<h3><span class="orange">Starting From Scratch</span></h3>
					<p>Although BNLM has an existing site, it does not adequately describe the range of services, activities, and public outreach the ministry provides. Additionally, the site is not optimized for SEO, it’s not responsive, and many of its senior-aged visitors — a target audience group — have difficulty locating the donate button. After conducting a content inventory of the current site, I decided that the site required a redesign from the ground up.</p>
					<h3><span class="orange">Pre-Production</span></h3>
					<p>In order to determine the necessary content and site structure, I spent six weeks in a discovery and information architecture phase; established the target audience; wrote user scenarios; compiled a competitive analysis, designed wire frames; designed a sitemap; created flowcharts and design mockups; and prototyping.</p>
