					<p>In light of these challenges, I saw my role as providing the most user-friendly and visually appealing website possible within the client parameters I was given.</p> 
					<p>For example, as a first step, I conducted a content inventory of the original site. The goal was to make navigation easier by grouping related content, identifying and streamline redundant text, and expanding sections that were lacking enough description.</p>
