				<h2>Project Details</h2>
				<img class="animated fadeInLeft" src="images/dr-he-gant.jpg" alt="Gantt chart">
				<p><span class="bold">Methodology:</span> For a design methodology I went with waterfall because the production team – from the photographer to the web designer to the graphic designer – worked remotely in three countries. Additionally, because we only had the web developer for couple weeks, everything needed to be defined and approved up front, which made an iterative methodology impractical.</p>
				<p><span class="bold">The Platform:</span> The client would need to make updates to the content periodically once the site was completed, so I contracted a Wordpress developer to build the site.</p> 
				<p><span class="bold">Responsive Design:</span> Although implementing responsive design was still a relatively new idea, the company decided not to take my recommendation to move ahead with it due to cost constraints.</p>
				<p><span class="bold">Budget &amp; content:</span> There were many challenges associated with the project, but the two greatest hurdles were working within a constrained budget that was too small to implement a best-practice approach, and having no defined content, both of which I'll discuss below.</p>
