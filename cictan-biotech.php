				<h2><span class="orange">The Challenges</span></h2>
				<p>The company didn’t have a creative direction in mind when it made the decision to redesign the site. As such, multiple mockups and wireframes had to be completed with very little creative guidance, which meant we spent a lot of time iterating.</p>
				<p>The biggest challenge was balancing what I knew to be industry best practices with project limitations, including budgetary constraints and the convoluted internal approvals process.</p>
				<p>For example, although I strongly recommended implementing effective SEO to the company, its leadership decided to only include a heading tag and descriptive language on the homepage for devices of tablet size and smaller – this was an aesthetic choice.</p>
				<p>Additionally, because content was not predefined, I was constantly coordinating with the writer, the translator, the lead scientist (to approve the accuracy of laymen’s language) and the web designer, all of whom were making constant changes.</p>
				<img class="animated slow fadeInUp" src="images/cictan-wire-frames.png" alt="Wireframes compared to the finished product.">
